<template>
<div :class="[task.reminder ? 'reminder' : '', 'task']"> <!--If task.reminder is true add a class of 'reminder' else don't do anything.The 'task' class is always there.-->
    <h3>
        {{task.text}}
        <i @click="onDelete(task.id)" class="fas fa-times"></i>
    </h3>
    <p>{{task.day}}</p>
</div>
</template>

<script>
export default {
    name: "Task",
    props: {
        task: Object,
    },
    methods: {
        onDelete(id) {
            this.$emit('delete-task' , id)
        }
    }
}

</script>

